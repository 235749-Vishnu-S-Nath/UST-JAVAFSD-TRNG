public class IfElseLadder {
	public static void main(String arg[]) {
		int number=0;
		if(number>0) {
			System.out.println("The number is postive");
		}
		else if(number<0) {
			System.out.println("The number is negative");
		}
		else {
			System.out.println("The number is 0");
		}
	}
}